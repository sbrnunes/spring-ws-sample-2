package com.bnpparibas.goe.service.edservice.proxy;

import org.apache.commons.httpclient.Credentials;
import org.apache.commons.httpclient.HttpState;
import org.apache.commons.httpclient.auth.AuthScope;

public class ProxyHttpState extends HttpState {

	public ProxyHttpState(Credentials credentials) {
		setProxyCredentials(AuthScope.ANY, credentials);
	}
	
	public void setProxyCredentials(Credentials credentials) {
		setProxyCredentials(AuthScope.ANY, credentials);
	}
	
}
